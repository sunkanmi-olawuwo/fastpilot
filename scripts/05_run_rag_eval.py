"""Production parity eval (AC4.1) — the golden 12 through POST /query, cache off.

The Week-4 numbers were scored on *offline* rag_results. This re-scores the SAME
12 golden questions on answers GENERATED BY PRODUCTION — driven through the real
``POST /query`` endpoint (guard → rewrite → route → hybrid retrieve → rerank →
Gemini), cache off — so the metric reflects what users actually get, prompt
changes and all.

It reuses the *exact* Week-4 instruments (apples-to-apples, no re-implementation):
  - faithfulness  : the v3 decomposed Gemini judge (`run_decomposed_judge.judge_answer`)
                    — extract atomic claims from the PRODUCTION answer, verify each
                    against the retrieved contexts; faithfulness = supported / total.
  - answer_coverage: the deterministic Voyage rerank-2.5 metric
                    (`run_semantic_metrics.greedy_match_fraction`) — fraction of
                    reference-answer sentences attributable to a retrieved context.

Baseline (Week-4 offline T1b): faithfulness 0.952 / answer_coverage 0.941.
Gate (AC4.1): both ≥ 0.90; a small degradation from the production prompt is
acceptable but the delta must be explained honestly. Writes
evaluations/eval_results/production_parity.json; exits non-zero below the gate.

Usage (from repo root):
    uv run python scripts/05_run_rag_eval.py
"""

from __future__ import annotations

import asyncio
import json
import sys
import time
from pathlib import Path
from statistics import mean

import _bootstrap  # noqa: F401  (final-submission on path + trust store)

# Reuse the EXACT Week-4 judge + metric code so the comparison is apples-to-apples.
_W4 = Path(__file__).resolve().parents[2] / "week-4" / "scripts"
sys.path.insert(0, str(_W4))
import _eval_common as ec  # noqa: E402
from run_decomposed_judge import judge_answer  # noqa: E402
from run_semantic_metrics import greedy_match_fraction  # noqa: E402

_SUB = Path(__file__).resolve().parent.parent
OUT = _SUB / "evaluations" / "eval_results" / "production_parity.json"
BASELINE = {"faithfulness": 0.952, "answer_coverage": 0.941}
GATE = 0.90
THRESHOLD = 0.5  # Voyage rerank relevance threshold (same as Week-4 semantic metrics)


async def _collect_answers(golden: dict) -> dict:
    """Drive every golden question through the real /query (cache off), capturing
    the production answer + retrieved contexts."""
    from app.main import create_app
    from asgi_lifespan import LifespanManager
    from httpx import ASGITransport, AsyncClient

    app = create_app()
    out: dict = {}
    async with LifespanManager(app):  # builds the REAL pipeline/cache/conversation
        transport = ASGITransport(app=app)
        async with AsyncClient(transport=transport, base_url="http://t", timeout=120.0) as client:
            for qid, entry in golden.items():
                q = ec.question_text(entry)
                resp = await client.post("/query", json={"query": q, "use_cache": False})
                resp.raise_for_status()
                body = resp.json()
                meta = body.get("metadata", {})
                out[qid] = {
                    "question": q,
                    "answer": body["answer"],
                    "contexts": [c.get("content", "") for c in body.get("contexts", [])],
                    "reference_answer": entry["reference_answer"],
                    "cache_hit": meta.get("cache_hit"),
                    "query_type": meta.get("query_type"),
                }
                print(f"  [gen] {qid:<4} {len(body['answer'])} chars, "
                      f"{len(out[qid]['contexts'])} contexts, type={meta.get('query_type')}")
    return out


def main() -> int:
    ec.load_environment(require=["GOOGLE_API_KEY", "VOYAGE_API_KEY", "QDRANT_API_KEY"])
    golden = ec.golden_by_qid(ec.load_golden_dataset())
    print(f"Generating production answers for {len(golden)} golden questions (cache off)…")
    answers = asyncio.run(_collect_answers(golden))

    vclient = ec.voyage_client()
    per_q = []
    print("\nScoring — v3 decomposed Gemini judge (faithfulness) + Voyage (answer_coverage):")
    for qid, a in answers.items():
        judged = judge_answer("v3", a["question"], a["answer"], a["reference_answer"], a["contexts"],
                              call=ec.call_gemini_json)
        ref_sents = ec.split_sentences(a["reference_answer"])
        coverage, n_attr = greedy_match_fraction(vclient, ref_sents, a["contexts"], THRESHOLD)
        row = {
            "question_id": qid,
            "faithfulness": judged["faithfulness"],
            "completeness": round(judged["completeness"], 3),
            "answer_coverage": round(coverage, 4),
            "n_claims": judged["detail"]["n_claims"],
            "n_supported": judged["detail"]["n_supported"],
            "n_answer_sentences": len(ref_sents),
            "n_attributable": n_attr,
            "query_type": a["query_type"],
        }
        per_q.append(row)
        print(f"  {qid:<4} faithfulness={row['faithfulness']:.3f} "
              f"coverage={row['answer_coverage']:.3f} "
              f"(claims {row['n_supported']}/{row['n_claims']})")
        time.sleep(1)

    agg = {
        "faithfulness": round(mean(q["faithfulness"] for q in per_q), 4),
        "answer_coverage": round(mean(q["answer_coverage"] for q in per_q), 4),
        "completeness": round(mean(q["completeness"] for q in per_q), 4),
    }
    summary = {
        "method": "production_parity: Week-4 v3 decomposed judge + Voyage semantic, via POST /query (cache off)",
        "n": len(per_q),
        "judge_model": ec.GEMINI_MODEL,
        "baseline_offline_t1b": BASELINE,
        "gate": GATE,
        "aggregate": agg,
        "delta_vs_baseline": {k: round(agg[k] - BASELINE[k], 4) for k in BASELINE},
        "per_question": per_q,
    }
    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(json.dumps(summary, indent=2))

    print("\n" + "=" * 64)
    for k in ("faithfulness", "answer_coverage"):
        d = agg[k] - BASELINE[k]
        print(f"  {k:<16}: {agg[k]:.3f}   baseline {BASELINE[k]}   Δ {d:+.3f}   gate {GATE}")
    print("=" * 64)
    ok = agg["faithfulness"] >= GATE and agg["answer_coverage"] >= GATE
    print("  PASS" if ok else "  FAIL — explain the delta honestly in production-decisions.md")
    return 0 if ok else 1


if __name__ == "__main__":
    raise SystemExit(main())
